def train_one_epoch_lgen(model: torch.nn.Module,
                    data_loader: Iterable, optimizer: torch.optim.Optimizer,
                    device: torch.device, epoch: int, loss_scaler,
                    log_writer=None,
                    args=None):
    model.train(True)
    metric_logger = misc.MetricLogger(delimiter="  ")
    metric_logger.add_meter('lr', misc.SmoothedValue(window_size=1, fmt='{value:.6f}'))
    header = 'Epoch: [{}]'.format(epoch)
    print_freq = 20

    accum_iter = args.accum_iter

    optimizer.zero_grad()

    if log_writer is not None:
        print('log_dir: {}'.format(log_writer.log_dir))

    blank_lang = torch.zeros(1, model.max_lang_len, dtype=torch.int64)
    blank_lang_mask = torch.zeros(1, model.max_lang_len, dtype=torch.int64)
    blank_lang[0][0], blank_lang_mask[0][0] = 101, 1
    
    for data_iter_step, (samples, texts, text_masks) in enumerate(metric_logger.log_every(data_loader, print_freq, header)):

        # we use a per iteration (instead of per epoch) lr scheduler
        if data_iter_step % accum_iter == 0:
            lr_sched.adjust_learning_rate(optimizer, data_iter_step / len(data_loader) + epoch, args)

        #samples, text, text_mask ([bsz, 3, 224, 224]) torch.Size([bsz, max_length]) torch.Size([bsz, max_length])

        # Replace the index rows in langs corresponding to lang_gen_weight with blank_lang.
        
        lang_gen_weight = torch.randint(0, 2, size=(samples.shape[0],))
        texts_cond = texts.clone()
        texts_cond[lang_gen_weight==1]  = blank_lang
        text_masks_cond = text_masks.clone()
        text_masks_cond[lang_gen_weight==1]  = blank_lang_mask

        samples = samples.to(device, non_blocking=True)
        texts = texts.to(device, non_blocking=True)
        text_masks = text_masks.to(device, non_blocking=True)
        texts_cond = texts_cond.to(device, non_blocking=True)
        text_masks_cond = text_masks_cond.to(device, non_blocking=True)
        lang_gen_weight = lang_gen_weight.to(device, non_blocking=True)
        # countdown()
        with torch.cuda.amp.autocast():
            loss, _, _ = model(samples, texts_cond, text_masks_cond, texts, text_masks, lang_gen_weight, mask_ratio=args.mask_ratio)
        # countdown()
        loss_value = loss.item()

        if not math.isfinite(loss_value):
            print("Loss is {}, stopping training".format(loss_value))
            sys.exit(1)

        loss /= accum_iter
        loss_scaler(loss, optimizer, parameters=model.parameters(),
                    update_grad=(data_iter_step + 1) % accum_iter == 0)
        if (data_iter_step + 1) % accum_iter == 0:
            optimizer.zero_grad()

        torch.cuda.synchronize()

        metric_logger.update(loss=loss_value)

        lr = optimizer.param_groups[0]["lr"]
        metric_logger.update(lr=lr)

        loss_value_reduce = misc.all_reduce_mean(loss_value)
        if log_writer is not None and (data_iter_step + 1) % accum_iter == 0:
            """ We use epoch_1000x as the x-axis in tensorboard.
            This calibrates different curves when batch size changes.
            """
            epoch_1000x = int((data_iter_step / len(data_loader) + epoch) * 1000)
            log_writer.add_scalar('train_loss', loss_value_reduce, epoch_1000x)
            log_writer.add_scalar('lr', lr, epoch_1000x)


    # gather the stats from all processes
    metric_logger.synchronize_between_processes()
    print("Averaged stats:", metric_logger)
    return {k: meter.global_avg for k, meter in metric_logger.meters.items()}